package Day06_Opertors;

public class EvenOrOdd {
    public static void main(String[] args){
        System.out.println(4%2); //0 is even
        System.out.println(5%2);//1 is odd

        System.out.println(1000%2); //0 = even

        int num = 55;
        System.out.println(num % 2);//1 is remainder (odd)



    }
}
