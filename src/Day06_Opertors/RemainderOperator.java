package Day06_Opertors;

public class RemainderOperator {
    public static void main(String[] args){
        System.out.println(10/3); //3
        System.out.println (10 % 3); //1
        //5+5+5+5
        System.out.println(20%5);//0 - no remainder
        //5+5+5+4
        System.out.println(19% 5);//4
        System.out.println(5 %2); //1
        //2+2
        System.out.println(2 % 5);//2 b/c smaller # is considered reminder



    }
}
