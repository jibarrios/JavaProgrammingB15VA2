package day02_introduction_to_variables;

public class VariableDeclarations {
    public static void main(String[] args){
        // declare single variable
        int apples;

        // declare multiple variables in single statements
        int bananas, oranges, mangoes,lemons;

        //declare single variable & assign value in 1 statement
        int kiwi = 2;

        // declare & assign value to multiple variables
        // in single statement
        int watermelons = 4, grapes = 45, cherries = 33, apricots =0;

    }
}
