package Day18_forloop;

public class SumOfNumbers {
    public static void main(String[] args) {
       // 1+2+3+4+5=15

        int sum = 0;

        for(int num2 = 1; num2<= 5; num2++) { // needs to be inside the curl brackets
            sum += num2; // sum = sum + num;
        }
        System.out.println("sum: "+ sum);




    }
}
